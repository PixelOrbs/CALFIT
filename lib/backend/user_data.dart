// user_data.dart
class UserData {
  String firstName;
  String lastName;
  String email;
  String password;
  String Age;
  String weight;
  String height;

  UserData({
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.password,
    required this.Age,
    required this.weight,
    required this.height,
  });
}
